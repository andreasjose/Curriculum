    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String num = jTextID.getText();
        
        if(!num.equals(""))
        {
            List<Produto> produtos = new ArrayList<>();
        
            ProdutoDAO dao = new ProdutoDAO();
            produtos = dao.Select(num);
        
            if(produtos.size() == 1)
            {
                int idcategoria;
                idcategoria = preenchecategoria(produtos);
                
                preenchedescricao(produtos, idcategoria);
                
                jTextField4.enable(true);
                jTextField4.setText(Integer.toString(produtos.get(0).getQuantidadecomprada()));
                
                jTextField5.enable(true);
                jTextField5.setText(Integer.toString(produtos.get(0).getQuantidadeestoque()));
                
                jMoneyField1.enable(true);
                jMoneyField1.setText(Float.toString(produtos.get(0).getPrecocompra()));
                
                jMoneyField2.enable(true);
                jMoneyField2.setText(Float.toString(produtos.get(0).getPrecovenda()));
                
                jFormattedTextField1.enable(true);
                jFormattedTextField1.setText(produtos.get(0).getDatavalidade().replace("-", "/"));
                
                jButton2.enable(true);
            }
            else
            {
                Mensagemcampoincompleto mensagem = new Mensagemcampoincompleto("Produto Inexistente");
                areatra.add(mensagem);
                mensagem.setVisible(true);
            }
        }
        else
        {
                Mensagemcampoincompleto mensagem = new Mensagemcampoincompleto("Digite um ID");
                areatra.add(mensagem);
                mensagem.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

