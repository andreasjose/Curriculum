    public int preenchecategoria(List<Produto> produtos)
    {
        int idcategoria = 0;
        jComboBox1.enable(true);
        jComboBox1.removeAllItems();
        jComboBox1.addItem(produtos.get(0).getCategoria().getDescricao());
        CategoriaDAO cat = new CategoriaDAO();
        for(Categoria c : cat.Select())
        {
            if(!produtos.get(0).getCategoria().getDescricao().equals(c.getDescricao()))
            {
                        jComboBox1.addItem(c.getDescricao());
            }
            else
            {
                idcategoria = produtos.get(0).getCategoria().getId();
            }
        }
        return idcategoria;
    }
    
    public void preenchedescricao(List<Produto> produtos, int idcategoria)
    {
        String produtonome = produtos.get(0).getDescricao().getDescricao();
        jComboBox2.enable(true);
        jComboBox2.removeAllItems();
        jComboBox2.addItem(produtonome);
        
        ProdutoDAO dao = new ProdutoDAO();
        for(Produto p : dao.Select())
        {
            if(p.getCategoria().getId() == idcategoria && !p.getDescricao().getDescricao().equals(produtonome))
            {
                jComboBox2.addItem(p.getDescricao().getDescricao());
            }
        }
    }

